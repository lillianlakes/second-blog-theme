function emailSubmitted() {
  var txt;
  if (confirm("I look forward to keeping you up-to-date on my travel adventures!  By providing your email address, you agree to receive travel update emails from me. Press OK to proceed or cancel if you prefer not to be emailed. Thank you.")) {
    txt = "Thank you for joining my email list.";
  } else {
    txt = "I am sorry you chose not to join my email list.  Please keep in touch.";
  }
  document.getElementById("emailConfirmation").innerHTML = txt;
}

